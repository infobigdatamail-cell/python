# Pide al usuario que ingrese un número entero que represente la vidaActual del personaje.
# Evalúa el valor ingresado:
# Si vidaActual es menor o igual a 0, muestra por consola:
#   "Game Over"
# En caso contrario, muestra por consola:
#   "Sigues en partida"

# Se crea la variable local
vidaActual = 0

# Se usa la estructura condicional
if vidaActual <= 0:
    print ('Game Over')
else:
    print ('Sigues en partida')
